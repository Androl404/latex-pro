            <p>Cookies are not the only way to recognize or track visitors to a website. Another similar technologies are web beacons (sometimes called “tracking pixels” or “clear gifs”). These are tiny graphics files that contain a unique identifier that enables to recognize when someone has visited a website or opened an email including them. This allows, for example, to monitor the traffic patterns of users from one page within a website to another, to deliver or communicate with cookies, to understand whether you have come to the website from an online advertisement displayed on a third-party website, to improve site performance, and to measure the success of email marketing campaigns. In many instances, these technologies are reliant on cookies to function properly, and so declining cookies will impair their functioning. It is important to understand that we do not use any of the technologies mentionned above.</p>
